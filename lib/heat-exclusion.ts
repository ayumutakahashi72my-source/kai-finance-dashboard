import type { Transaction } from '@/lib/types'

/**
 * カレンダーの支出ヒートマップから除外すべき取引かどうか。
 *
 * 目的: 「日々の変動支出の濃淡」を見せるため、金額が大きく日付が機械的な
 * まとめ引き落とし（クレカ請求・ローン・奨学金・積立投資・通信キャリア）を除外する。
 *
 * 注意:
 * - payee 文字列マッチによるヒューリスティックであり、誤爆はあり得る
 *   （例: 「カード」を含む店舗名）。除外はヒートマップの見た目のみに影響し、
 *   収支合計・カテゴリ集計には一切使わないこと。
 * - キーワードは日本の主要カード会社・引き落とし系の一般名詞に限定する。
 *   世帯固有の店舗名を足したくなったら、excluded / is_fixed フラグ側で
 *   解決できないかを先に検討する。
 * - MFのCSVはカタカナ長音を半角ハイフン「-」や全角「－」で表すことがあるため、
 *   各種ハイフン・ダッシュを「ー」に正規化してから比較する。
 */
const HEAT_EXCLUDE_KEYWORDS = [
  // クレジットカード・信販（全国区ブランド）
  'クレジット', 'カード',
  'ジエーシービ',  // JCB（ジエ-シ-ビ- → 正規化後）
  'オリコ',
  'スミトモ',      // 三井住友クレジット
  'クオーク',      // 三井住友クレジット（クオーク）
  'ペイデイ',      // PayDay決済
  // ローン・奨学金
  'ローン', 'ガクセイシエン', '奨学金',
  // 投資・積立
  '証券', '投信', '積立', '投資',
  // 通信キャリアのまとめ請求
  '携帯電話',
]

export function isHeatExcluded(tx: Pick<Transaction, 'payee' | 'is_fixed'>): boolean {
  if (tx.is_fixed) return true
  const p = tx.payee.replace(/[-－‐–—ｰ]/g, 'ー')
  return HEAT_EXCLUDE_KEYWORDS.some((k) => p.includes(k))
}
